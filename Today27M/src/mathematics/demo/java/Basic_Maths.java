package mathematics.demo.java;

public class Basic_Maths {

	static void addition(int a,int b)
	{
		int c = a+b;
		System.out.println("Addition is:"+c);
	}
	
	static void substraction(int a,int b)
	{
		int c = a-b;
		System.out.println("Substarction is:"+c);
	}
	
	static void multiplication(int a,int b)
	{
		int c = a*b;
		System.out.println("Multiplication is:"+c);
	}
	
	static void divistion(int a,int b)
	{
		int c = a/b;
		System.out.println("Divistion is:"+c);
	}
	
}
